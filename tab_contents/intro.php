<h2><img src="images/icons/help.png" alt="Question Mark Icon" /> What is <?php echo SEARCH_NAME; ?>?</h2>
<p>Searching many individual databases for the information you need can be tedious and frustrating. To simplify the early research process, COCC Barber Library developed this interface for one-stop searching of many different resources.</p>
<p>Based on the structures of search engines like Google, Yahoo, and Bing, Library Search Beta presents results from our databases in six resource type tabs:</p>
<ol>
  <li><span style="font-weight:bold;">Articles &amp; More:</span> Search Barber Library holdings, reference articles, scholarly articles, and guides</li>
  <li><span style="font-weight:bold;">Books:</span> Search for print books and eBooks</li>
  <li><span style="font-weight:bold;">Videos &amp; Audio:</span> Search for DVDs, streaming videos, and audio CDs</li>
  <li><span style="font-weight:bold;">Images:</span> Search for relevant images to use in papers and presentations</li>
  <li><span style="font-weight:bold;">News:</span> Search newspapers, magazines, and pro/con articles</li>
  <li><span style="font-weight:bold;">Statistics:</span> Search for statistics and data-driven graphics</li>
</ol>

<h2><img src="images/icons/search.png" alt="Magnifying Glass Icon" /> What am I searching here?</h2>
<p>Though this interface searches many databases at once, it does not search <em>every</em> database available through Barber Library.</p>
<p>For each resource type, we have selected one popular database that returns useful and comprehensive results. For example, in the default "Articles &amp; More" tab...</p>
<ul>
  <li>The Books, Videos, &amp; More box displays results from the <a href="https://barber.cocc.edu/catalog/">Barber Library &amp; Summit Catalog</a>.</li>
  <li>The Reference Articles box displays results from <a href="http://guides.cocc.edu/databases/credo">Credo Reference</a>.</li>
  <li>The Scholarly Articles box displays results from Ebsco's <a href="http://guides.cocc.edu/databases/academic-search-premier">Academic Search Premier</a>.</li>
</ul>
<p>If the database we chose has no results for your keyword(s), the box will instead offer a link to another database that might have the information you're looking for. For example, if Credo Reference has no results, a link will appear labeled, "Search for {your keywords} in <a href="http://guides.cocc.edu/databases/gvrl">Gale Virtual Reference Library</a>."</p>
<p>Each tab also offers recommended databases in colorful boxes at the bottom of the page.</p>
<p>For more advanced searching, see the comprehensive list of COCC research databases at <a href="http://guides.cocc.edu/az.php">Articles &amp; Databases</a>.</p>


